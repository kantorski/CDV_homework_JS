/*
Treść zadania:
Napisz funkcję obliczającą Liczbę Trójkątną o numerze przekazanym jako argument.
*/

const triangleNumber = (n) => ((n * (n + 1)) / 2)

/*
Wersja zaawansowana:
Funkcja powinna obliczać Liczbę Czworościenną.
*/

const quadrangleNumber = (n) => ((n * (n + 1) * (n + 2)) / 6)